#include <stdio.h>

/*
Escreva um programa em C para exibir os primeiros 10 números naturais.
*/

int main() {
    // Loop for para exibir os primeiros 10 números naturais
    printf("Os primeiros 10 números naturais são:\n");
    for (int i = 1; i <= 10; i++) {
        printf("%d\n", i);
    }

    return 0;
}